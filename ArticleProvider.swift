import Foundation

struct ArticleProvider {
    
    static func getContent(for title: String) -> String {
        switch title {
        case "Управление стрессом":
            return """
            КОНЦЕПЦИЯ БЕНСОНА
            
            Гарвардский профессор Герберт Бенсон доказал, что у человека есть встроенный механизм расслабления, который снижает уровень кортизола.
            
            ДВА УСЛОВИЯ УСПЕХА
            
            1. Повторяющийся стимул (слово или дыхание).
            2. Пассивное отношение к мыслям.
            
            МЕТОД РАБОТЫ
            
            Сядьте удобно и расслабьте все мышцы. Дышите через нос. На каждом выдохе произносите про себя слово «ОДИН». 
            
            Если приходят посторонние мысли — не злитесь. Просто отметьте это и мягко вернитесь к повторению слова. Продолжайте 10–20 минут.
            
            Источник: на основе трудов Г. Бенсона (Harvard Medical School).
            """
        case "Медитация для начинающих":
            return """
            ОСНОВЫ ОСОЗНАННОСТИ
            Медитация — это не попытка «очистить» разум от мыслей. Это практика наблюдения за тем, что происходит в данный момент, без вынесения оценок. 
            
            ГДЕ И КАК СИДЕТЬ
            Вам не нужна специальная одежда или подушки. Главное — прямая спина. Это позволяет сохранять бдительность и не засыпать. Положите руки на колени и слегка прикройте глаза.
            
            ФОКУС НА ДЫХАНИИ
            Выберите точку, где вы чувствуете дыхание отчетливее всего: кончик носа, грудная клетка или подъем живота. Просто наблюдайте за тем, как воздух входит и выходит.
            
            ЧТО ДЕЛАТЬ С МЫСЛЯМИ
            Ваш мозг обязательно начнет генерировать мысли. Это его работа. Когда вы поймаете себя на том, что планируете ужин или вспоминаете старую ссору, просто отметьте: «О, я думаю». И мягко, без самокритики, вернитесь к дыханию.
            
            ПРАКТИКА 5 МИНУТ
            Начните с малого. Даже 5 минут осознанного сидения в день тренируют «мышцу внимания» эффективнее, чем час медитации раз в неделю.
            
            Источник: на основе программы MBSR доктора Джона Кабат-Зинна (Массачусетский университет).
            """
            
        default:
            return "Текст статьи скоро появится..."
        }
    }
}
